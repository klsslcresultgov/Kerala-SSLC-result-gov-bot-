# Function to create Professional PDF
def create_pdf(reg_no, student_name, results, filename):
    pdf = FPDF()
    pdf.add_page()
    
    # Header
    pdf.set_font("Arial", 'B', 18)
    pdf.cell(200, 10, txt="SSLC EXAMINATION RESULTS 2026", ln=True, align='C')
    pdf.set_font("Arial", size=12)
    pdf.cell(200, 10, txt="Government of Kerala", ln=True, align='C')
    pdf.ln(10)

    # Student Details
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, txt=f"Name: {student_name}", ln=True)
    pdf.cell(0, 10, txt=f"Register Number: {reg_no}", ln=True)
    pdf.ln(5)

    # Table Header
    pdf.set_fill_color(200, 220, 255)
    pdf.set_font("Arial", 'B', 11)
    pdf.cell(130, 10, "Subject", 1, 0, 'C', True)
    pdf.cell(40, 10, "Grade", 1, 1, 'C', True)

    # Table Content
    pdf.set_font("Arial", size=11)
    for subject, grade in results.items():
        pdf.cell(130, 10, subject, 1)
        pdf.cell(40, 10, grade, 1, 1, 'C')

    # Footer
    pdf.ln(20)
    pdf.set_font("Arial", 'I', 8)
    pdf.cell(0, 10, "Generated by Kerala SSLC Result Bot (Unofficial)", 0, 0, 'C')
    
    pdf.output(filename)

